const ThemeContext = createContext(null)

function applyTheme(theme) {
  const root = document.documentElement
  const vars = {
    '--page-bg':           theme.pageBg,
    '--card-bg':           theme.cardBg,
    '--card-border':       theme.cardBorder,
    '--card-shadow':       theme.cardShadow,
    '--card-glow':         theme.cardGlow,
    '--header-border':     theme.headerBorder,
    '--th-bg':             theme.tableHeadBg,
    '--th-text':           theme.tableHeadText,
    '--kpi-bg':            theme.kpiBg,
    '--kpi-border':        theme.kpiBorder,
    '--primary':           theme.primary,
    '--primary-dark':      theme.primaryDark,
    '--btn-from':          theme.btnFrom,
    '--btn-to':            theme.btnTo,
    '--btn-hover-from':    theme.btnHoverFrom,
    '--btn-hover-to':      theme.btnHoverTo,
    '--badge-primary-bg':  theme.badgePrimaryBg,
    '--badge-primary-text':theme.badgePrimaryText,
    '--scroll-thumb':      theme.scrollThumb,
    '--input-focus':       theme.inputFocus,
    '--accent':            theme.accent,
  }
  Object.entries(vars).forEach(([k, v]) => root.style.setProperty(k, v))
  document.body.style.backgroundColor = theme.pageBg
}

export function ThemeProvider({ children }) {
  const [themeId, setThemeId] = useState(() => localStorage.getItem('app-theme') || 'gold')
  const theme = THEMES.find(t => t.id === themeId) || THEMES[0]

  useEffect(() => {
    applyTheme(theme)
    localStorage.setItem('app-theme', themeId)
  }, [theme, themeId])

  const changeTheme = useCallback((id) => setThemeId(id), [])

  return (
    <ThemeContext.Provider value={{ theme, themes: THEMES, changeTheme, themeId }}>
      {children}
    </ThemeContext.Provider>
  )
}

export function useTheme() {
  const ctx = useContext(ThemeContext)
  if (!ctx) throw new Error('useTheme must be used within ThemeProvider')
  return ctx
}
